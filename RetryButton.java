import java.awt.Rectangle;

// This class will create a retry button object that will be used to restart the game.
public class RetryButton extends Rectangle{
    /**
     * Constructor for the RetryButton class
     * @param x the x coordinate of the retry button
     * @param y the y coordinate of the retry button
     * @param width the width of the retry button
     * @param height the height of the retry button
     */
    RetryButton (int x, int y, int width, int height){
        super(x, y, width, height);
    }
}
